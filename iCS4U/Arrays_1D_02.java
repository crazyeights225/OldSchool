public class Arrays_1D_02 {
	public static void main (String[] args)
	{
		int [][] n=new int[10][15];
		
        //Columns
		for(int i=n.length;i>0;i--){
		//Rows
		for(int j= n.length;j>0;j--){
			
			System.out.print("*");
		}
		System.out.println();
		
	}}
}
